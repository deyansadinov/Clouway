public class Sumator {

  /**
   * Calculate the sum of two numbers
   * @param a the first number
   * @param b the second number
   * @return the sum
   */
  public int sum(String a, String b){
    return Integer.parseInt(a) + Integer.parseInt(b);
  }
}
